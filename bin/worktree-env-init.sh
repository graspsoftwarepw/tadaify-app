#!/usr/bin/env bash
# bin/worktree-env-init.sh — Idempotent local env bootstrap for fresh worktrees.
#
# Usage: ./bin/worktree-env-init.sh [--quiet]
#
# What it does:
#   1. Verifies CWD is a git working tree.
#   2. .env: copies .env.example if absent; generates a valid
#      BEFORE_USER_CREATED_HOOK_SECRET if the current value is a placeholder.
#   3. .dev.vars: copies .dev.vars.example if absent; fills in
#      SUPABASE_URL / SUPABASE_ANON_KEY / SUPABASE_SERVICE_ROLE_KEY from
#      `supabase status -o env` (requires Supabase to be running).
#      Adds HANDLE_RESERVATION_TTL_SECONDS=600 if the line is missing.
#
# Idempotent: re-running is safe — already-valid values are left untouched.
# Exits 1 with a clear error message on any unrecoverable condition.
#
# macOS + Linux compatible (uses `sed -i.bak` + `rm *.bak` pattern).

set -euo pipefail

QUIET=0
for arg in "$@"; do
  [[ "$arg" == "--quiet" ]] && QUIET=1
done

log() { [[ "$QUIET" -eq 0 ]] && echo "$1" || true; }
err() { echo "ERROR: $1" >&2; exit 1; }

# ── 1. Verify CWD is a git working tree ─────────────────────────────────────

git rev-parse --git-dir >/dev/null 2>&1 || err "Not a git working tree — run this script from the repo root."

REPO_ROOT="$(git rev-parse --show-toplevel)"
cd "$REPO_ROOT"

log "Tadaify worktree env init"
log "  repo: $REPO_ROOT"
log ""

# ── 2. .env ──────────────────────────────────────────────────────────────────

ENV_FILE=".env"
ENV_EXAMPLE=".env.example"

[[ -f "$ENV_EXAMPLE" ]] || err "Missing $ENV_EXAMPLE — cannot bootstrap .env."

if [[ ! -f "$ENV_FILE" ]]; then
  cp "$ENV_EXAMPLE" "$ENV_FILE"
  log "  ✓ .env: created from .env.example"
fi

# Check if BEFORE_USER_CREATED_HOOK_SECRET has a valid value.
# Valid: starts with v1,whsec_ followed by ≥32 base64 chars.
HOOK_SECRET_LINE=$(grep '^BEFORE_USER_CREATED_HOOK_SECRET=' "$ENV_FILE" || true)
HOOK_SECRET_VALUE="${HOOK_SECRET_LINE#BEFORE_USER_CREATED_HOOK_SECRET=}"

if [[ "$HOOK_SECRET_VALUE" =~ ^v1,whsec_[A-Za-z0-9+/=]{32,}$ ]]; then
  log "  ✓ .env: BEFORE_USER_CREATED_HOOK_SECRET already valid"
else
  # Generate new secret
  NEW_SECRET="$(printf 'v1,whsec_%s' "$(openssl rand -base64 32)")"
  # sed -i.bak works on both macOS and Linux
  sed -i.bak "s|^BEFORE_USER_CREATED_HOOK_SECRET=.*$|BEFORE_USER_CREATED_HOOK_SECRET=$NEW_SECRET|" "$ENV_FILE"
  rm -f "$ENV_FILE.bak"
  log "  ✓ .env: BEFORE_USER_CREATED_HOOK_SECRET generated"
fi

# ── 3. .dev.vars ─────────────────────────────────────────────────────────────

DEV_VARS_FILE=".dev.vars"
DEV_VARS_EXAMPLE=".dev.vars.example"

if [[ ! -f "$DEV_VARS_FILE" ]]; then
  if [[ -f "$DEV_VARS_EXAMPLE" ]]; then
    cp "$DEV_VARS_EXAMPLE" "$DEV_VARS_FILE"
    log "  ✓ .dev.vars: created from .dev.vars.example"
  else
    # Fallback: inline template (source of truth for Workers env)
    cat > "$DEV_VARS_FILE" <<'DEVVARS'
# Workers runtime env bindings — generated by bin/worktree-env-init.sh
SUPABASE_URL=http://127.0.0.1:54351
SUPABASE_ANON_KEY=eyJ...replace-with-output-of-supabase-status
SUPABASE_SERVICE_ROLE_KEY=eyJ...replace-with-output-of-supabase-status
HANDLE_RESERVATION_TTL_SECONDS=600
DEVVARS
    log "  ✓ .dev.vars: created from inline template (.dev.vars.example not found)"
  fi
fi

# Check if Supabase keys look like placeholders
ANON_LINE=$(grep '^SUPABASE_ANON_KEY=' "$DEV_VARS_FILE" || true)
ANON_VALUE="${ANON_LINE#SUPABASE_ANON_KEY=}"

is_placeholder() {
  local val="$1"
  # Placeholder patterns: empty, contains "replace-with", starts with "eyJ...replace"
  [[ -z "$val" ]] || [[ "$val" == *"replace-with"* ]] || [[ "$val" == "eyJ...replace"* ]]
}

if is_placeholder "$ANON_VALUE"; then
  # Try to get real values from supabase status
  if ! supabase status >/dev/null 2>&1; then
    log "  ⚠ .dev.vars: Supabase is not running — keys left as placeholders."
    log "    Run \`supabase start\` then re-run this script to fill them in."
    SUPABASE_AVAILABLE=0
  else
    SUPABASE_AVAILABLE=1
  fi

  if [[ "${SUPABASE_AVAILABLE:-1}" -eq 0 ]]; then
    # Skip .dev.vars key population — will be filled on next run
    :
  elif ! STATUS_ENV="$(supabase status -o env 2>/dev/null)"; then
    log "  ⚠ .dev.vars: Failed to read supabase status — keys left as placeholders."
    log "    Re-run this script after Supabase is up."
  else
    # `supabase status -o env` key names differ across CLI versions:
    #   older CLI:  SUPABASE_URL / SUPABASE_ANON_KEY / SERVICE_ROLE_KEY
    #   newer CLI:  API_URL      / ANON_KEY           / SERVICE_ROLE_KEY
    # Strip optional surrounding quotes from values.
    extract_val() {
      local key="$1"
      local raw
      raw="$(echo "$STATUS_ENV" | grep "^${key}=" | head -1 | cut -d= -f2-)"
      # Strip leading/trailing double-quotes if present (newer CLI wraps values)
      raw="${raw#\"}"
      raw="${raw%\"}"
      echo "$raw"
    }

    # Try canonical keys first, fall back to alternate names
    SB_URL="$(extract_val SUPABASE_URL)"
    [[ -z "$SB_URL" ]] && SB_URL="$(extract_val API_URL)"

    SB_ANON="$(extract_val SUPABASE_ANON_KEY)"
    [[ -z "$SB_ANON" ]] && SB_ANON="$(extract_val ANON_KEY)"

    SB_SERVICE="$(extract_val SERVICE_ROLE_KEY)"

    [[ -n "$SB_URL" ]]     || err "supabase status did not return API_URL/SUPABASE_URL. Is Supabase running?"
    [[ -n "$SB_ANON" ]]    || err "supabase status did not return ANON_KEY/SUPABASE_ANON_KEY. Is Supabase running?"
    [[ -n "$SB_SERVICE" ]] || err "supabase status did not return SERVICE_ROLE_KEY. Is Supabase running?"

    sed -i.bak "s|^SUPABASE_URL=.*$|SUPABASE_URL=$SB_URL|" "$DEV_VARS_FILE"
    sed -i.bak "s|^SUPABASE_ANON_KEY=.*$|SUPABASE_ANON_KEY=$SB_ANON|" "$DEV_VARS_FILE"
    sed -i.bak "s|^SUPABASE_SERVICE_ROLE_KEY=.*$|SUPABASE_SERVICE_ROLE_KEY=$SB_SERVICE|" "$DEV_VARS_FILE"
    rm -f "$DEV_VARS_FILE.bak"
    log "  ✓ .dev.vars: Supabase keys populated from \`supabase status\`"
  fi
else
  log "  ✓ .dev.vars: Supabase keys already set"
fi

# Ensure HANDLE_RESERVATION_TTL_SECONDS line exists
if ! grep -q '^HANDLE_RESERVATION_TTL_SECONDS=' "$DEV_VARS_FILE"; then
  printf '\nHANDLE_RESERVATION_TTL_SECONDS=600\n' >> "$DEV_VARS_FILE"
  log "  ✓ .dev.vars: added HANDLE_RESERVATION_TTL_SECONDS=600"
fi

# ── 4. Summary ───────────────────────────────────────────────────────────────

log ""
log "Done. .env is ready."
if is_placeholder "$(grep '^SUPABASE_ANON_KEY=' "$DEV_VARS_FILE" 2>/dev/null | cut -d= -f2-)"; then
  log ".dev.vars still has placeholder keys — run \`supabase start\` then re-run this script."
else
  log ".dev.vars is ready."
fi
